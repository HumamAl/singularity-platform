export const getUserById = (id: string) =>
  systemUsers.find((u) => u.id === id);

export const getNodeById = (id: string) =>
  singularityNodes.find((n) => n.id === id);

export const getArtifactById = (id: string) =>
  vaultArtifacts.find((a) => a.id === id);

export const getClusterById = (id: string) =>
  neuralClusters.find((c) => c.id === id);

export const getNodesByCluster = (clusterId: string) =>
  singularityNodes.filter((n) => n.clusterId === clusterId);

export const getEventsByNode = (nodeId: string) =>
  convergenceEvents.filter((e) => e.relatedNodeId === nodeId);

export const getEventsByArtifact = (artifactId: string) =>
  convergenceEvents.filter((e) => e.relatedArtifactId === artifactId);

export const getAccessEventsByArtifact = (artifactId: string) =>
  vaultAccessEvents.filter((e) => e.artifactId === artifactId);

export const getSessionsByUser = (userId: string) =>
  userSessions.filter((s) => s.userId === userId);

export const getArtifactsByOwner = (ownerId: string) =>
  vaultArtifacts.filter((a) => a.ownerId === ownerId);
